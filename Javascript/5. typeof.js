// Tipos de datos en JavaScript

let miEntero = 10; // Entero
console.log(miEntero);
console.log(typeof miEntero); // Muestra el tipo de dato

miEntero = "Hola"; // Cambiando el tipo de dato a cadena
console.log(typeof miEntero);

let miFlotante = 7.9;
console.log(miFlotante);
console.log(typeof miFlotante); // Muestra el tipo de dato

let miCadena = "Hola, mundo"; // Cadena de texto
console.log(miCadena);
console.log(typeof miCadena); // Muestra el tipo de dato

let miBooleano = true; // Booleano
console.log(miBooleano);
console.log(typeof miBooleano); // Muestra el tipo de dato

let miNull = null; // Null
console.log(miNull);
console.log(typeof miNull); // Muestra el tipo de dato (object, es un caso especial en JavaScript)

let miUndefined; // Undefined
console.log(miUndefined);
console.log(typeof miUndefined); // Muestra el tipo de dato